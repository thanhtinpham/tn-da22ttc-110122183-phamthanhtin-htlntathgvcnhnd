            // Drag caught fish along
            if (caughtFish) {
                caughtFish.x = hookX - 42;
                caughtFish.y = hookY - 20;
                caughtFish.element.style.left = `${caughtFish.x}px`;
                caughtFish.element.style.top = `${caughtFish.y}px`;
            }

            // Returned to rod tip
            if (hookY <= tip.y) {
                hookState = 'idle';
                if (hookEl) hookEl.classList.add('hidden');
                linePath.setAttribute('d', '');

                if (caughtFish) {
                    caughtFish.element.classList.add('hidden');
                    const fishToJump = caughtFish;
                    caughtFish = null;
                    jumpFishUpward(fishToJump);
                }
            }
        }

        // 3. Update swimming fish
        fishList.forEach(fish => {
            if (!fish.isHooked) {
                // Update position
                fish.x += fish.vx;
                fish.element.style.top = `${fish.y + Math.sin(Date.now() / 400 + fish.randomOffset) * 6}px`;
                fish.element.style.left = `${fish.x}px`;

                // Bounce off edges
                if (fish.vx > 0 && fish.x > oceanW - 90) {
                    fish.vx = -fish.vx;
                    const img = fish.element.querySelector('img');
                    if (img) img.style.transform = 'scaleX(1)';
                } else if (fish.vx < 0 && fish.x < 10) {
                    fish.vx = -fish.vx;
                    const img = fish.element.querySelector('img');
                    if (img) img.style.transform = 'scaleX(-1)';
                }
            }
        });

        loopAnimId = requestAnimationFrame(gameLoopTick);
    };

    // Start Game flow
    const startGame = () => {
        initAudioContext();
        screenLaunch.classList.add('hidden');
        screenGameOver.classList.add('hidden');
        screenWin.classList.add('hidden');
        
        btnSoundPlay.disabled = false;
        isGameRunning = true;

        // Reset boat and hook state variables
        boatX = 10;
        boatDir = 1;
        hookState = 'idle';
        caughtFish = null;
        selectedFishObject = null;

        const hookEl = document.getElementById('fishing-hook');
        if (hookEl) hookEl.classList.add('hidden');

        const linePath = document.getElementById('fishing-line-path');
        if (linePath) linePath.setAttribute('d', '');

        activeQuestionIndex = 0;
        score = 0;
        coins = 0;
        lives = 3;
        combo = 0;

        renderHearts();
        updateHUD();
        spawnSwimmingFish();
        playLaserSound();
        setTimeout(playCurrentAudio, 300);

        if (loopAnimId) {
            cancelAnimationFrame(loopAnimId);
        }
        loopAnimId = requestAnimationFrame(gameLoopTick);
    };

    // Laser sound effect helper
    const playLaserSound = () => {
        synthBeep(587.33, 0.1, 'sine'); // D5
    };

    // End Game (Win or Lose)
    const endGame = (isWin) => {
        isGameRunning = false;
        cancelAnimationFrame(loopAnimId);
        loopAnimId = null;
        btnSoundPlay.disabled = true;
        targetWordAudio.pause();

        const hookEl = document.getElementById('fishing-hook');
        if (hookEl) hookEl.classList.add('hidden');

        const linePath = document.getElementById('fishing-line-path');
        if (linePath) linePath.setAttribute('d', '');

        if (isWin) {
            hudProgressBar.style.width = '100%';
            screenWin.classList.remove('hidden');
            playCorrectSound();
        } else {
            screenGameOver.classList.remove('hidden');
        }
    };

    // Submit backend form
    btnSubmit.addEventListener('click', (e) => {
        e.stopPropagation();
        document.getElementById('catch-word-form').submit();
    });

    // Event listeners
    btnStart.addEventListener('click', (e) => {
        e.stopPropagation();
        startGame();
    });
    btnReplay.addEventListener('click', (e) => {
        e.stopPropagation();
        startGame();
    });

    // Initial setup calls
    initCollectionUI();
    updateCollectionCount();
});
</script>
