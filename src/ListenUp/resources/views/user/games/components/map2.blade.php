<script>
document.addEventListener('DOMContentLoaded', () => {
    // 1. Data Loader from PHP Blade
    const questionsData = [
        @foreach($questions as $qIdx => $qData)
            {
                id: "{{ $qData['question']->MaCauHoi }}",
                audioUrl: "{{ $qData['audio'] }}",
                correctOptionId: "{{ $qData['correct_option'] ? $qData['correct_option']->MaPA : '' }}",
                correctText: "{{ $qData['correct_option'] ? preg_replace('/^[A-D]\.\s*/', '', $qData['correct_option']->NDPA) : '' }}",
                options: [
                    @foreach($qData['options'] as $opt)
                        {
                            id: "{{ $opt->MaPA }}",
                            text: "{{ preg_replace('/^[A-D]\.\s*/', '', $opt->NDPA) }}",
                            isCorrect: {{ $opt->DapAn === 'Dung' ? 'true' : 'false' }}
                        },
                    @endforeach
                ]
            },
        @endforeach
    ];

    // Web Audio Synthesizer for Cozy sound effects
    let audioCtx = null;
    const initAudioContext = () => {
        if (!audioCtx) {
            audioCtx = new (window.AudioContext || window.webkitAudioContext)();
        }
        if (audioCtx.state === 'suspended') {
            audioCtx.resume();
        }
    };

    const synthBeep = (freq, duration, type = 'sine', decay = true) => {
        if (!audioCtx) return;
        try {
            const osc = audioCtx.createOscillator();
            const gain = audioCtx.createGain();
            osc.connect(gain);
            gain.connect(audioCtx.destination);
            
            osc.type = type;
            osc.frequency.setValueAtTime(freq, audioCtx.currentTime);
            
            gain.gain.setValueAtTime(0.08, audioCtx.currentTime);
            if (decay) {
                gain.gain.exponentialRampToValueAtTime(0.001, audioCtx.currentTime + duration);
            }
            
            osc.start();
            osc.stop(audioCtx.currentTime + duration);
        } catch(e) {}
    };

